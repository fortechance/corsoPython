def Fibonacci(numero):
    sequenza = []

    a = 0
    b = 1

    for i in range(numero):

        fibonacci = a + b
        a = b
        b = fibonacci

        print(fibonacci)
        sequenza.append(fibonacci)

    return sequenza

print(Fibonacci(20))
